# Substrings that mark a research domain as biomedical (for bio_sources "auto").
_BIOMED_KEYWORD_MARKERS = (
    "biolog", "genom", "transcript", "rna", "dna", "protein", "gene", "cell",
    "clinical", "medical", "disease", "patient", "epigen", "chromatin",
    "methylation", "single-cell", "biomed", "neuro", "cancer", "immun",
    "microb", "drug", "molecul", "pathogen", "virus", "antibod",
)


def _bio_sources_enabled(config: Dict) -> bool:
    """Whether the biomedical sources (bioRxiv/medRxiv/PubMed) are searched.

    Explicit `bio_sources: true`/`false` always wins. Otherwise ("auto", or any
    unrecognised/absent value) the sources run ONLY when the config shows a
    biomedical signal — a `q-bio.*` arXiv category or a biomedical keyword. This
    fixes the QC finding that PubMed injected clinical-nutrition noise into a
    non-biomedical (humanitarian) persona, and that unknown values like
    "garbage" silently turned the sources on. A non-bio researcher who still
    wants PubMed can set `bio_sources: true`.
    """
    setting = config.get("bio_sources", "auto")
    if setting is True or setting == "true":
        return True
    if setting is False or setting == "false":
        return False
    for dom in (config.get("research_domains") or {}).values():
        for cat in (dom.get("arxiv_categories") or []):
            if str(cat).startswith("q-bio."):
                return True
        for kw in (dom.get("keywords") or []):
            kw_l = str(kw).lower()
            if any(marker in kw_l for marker in _BIOMED_KEYWORD_MARKERS):
                return True
    return False


# Broad cross-disciplinary arXiv categories, used only when neither --categories
# nor the config's research_domains supply any (keeps a bare run non-empty).
_FALLBACK_ARXIV_CATEGORIES = [
    "cs.AI", "cs.LG", "cs.CL", "cs.CV", "cs.NE", "stat.ML", "math.ST",
    "physics.comp-ph", "econ.GN", "q-bio.QM",
]


def _derive_arxiv_categories_from_config(config: Dict) -> List[str]:
    """Union of every domain's `arxiv_categories`, order-preserving and deduped.

    Makes the script field-agnostic on its own: previously this derivation lived
    only in SKILL.md's bash, so a direct (non-SKILL) invocation without
    --categories silently fell back to a CS-only default. Now `main()` calls this
    when --categories is absent.
    """
    cats: List[str] = []
    seen = set()
    for dom in (config.get("research_domains") or {}).values():
        for cat in (dom.get("arxiv_categories") or []):
            if cat not in seen:
                seen.add(cat)
                cats.append(cat)
    return cats


def _split_pool(unique_papers, pool_size, top_n):
    """Candidate pool for reranking + the keyword-only fallback list.

    `candidates` = top `pool_size` (>= top_n) papers, ranked. `top_papers` =
    first `top_n` of the pool — used directly when no rerank step runs.
    """
    size = max(pool_size, top_n)
    candidates = unique_papers[:size]
    return candidates, candidates[:top_n]


def _priority_weight(domains: Dict, domain_name: Optional[str]) -> float:
    """Per-domain `priority` (1–5) as a relevance multiplier for ranking.

    priority 3 is neutral (×1.0); 5 → ×1.67, 1 → ×0.33. This is the
    `priority: 1–5` the config has always documented ("weights the score") but
    never actually applied: it weights how a paper RANKS (its recommendation
    score), so a higher-priority topic's matches rise to the top. It does NOT
    change which papers clear the keyword-relevance gate.
    """
    if not domain_name:
        return 1.0
    raw = (domains.get(domain_name) or {}).get("priority", 3)
    try:
        p = float(raw)
    except (TypeError, ValueError):
        p = 3.0
    p = max(1.0, min(5.0, p))
    return p / 3.0


def _extra_source_enabled(spec: Dict, config: Dict) -> bool:
    """Decide whether an optional source (OpenAlex/Crossref/CORE) runs.

    Its config value may be a bool/"true"/"false", or a dict with an `enabled`
    key holding the same. "auto" (the default): keyless sources always run;
    keyed sources run only when their key_env resolves to a non-empty value.
    """
    raw = config.get(spec["cfg_key"], spec.get("default", "auto"))
    setting = raw.get("enabled", "auto") if isinstance(raw, dict) else raw
    if setting is True or setting == "true":
        return True
    if setting is False or setting == "false":
        return False
    if spec["key_env"] is None:
        return True  # keyless source — on by default under "auto"
    try:
        from _env_resolve import load_env_from_user_shell as _lenv
        _lenv((spec["key_env"],))
    except Exception:
        pass
    return bool(os.environ.get(spec["key_env"], "").strip())


def _resolve_scoring(config: Dict) -> Dict:
    """Merge the optional `scoring:` config block onto the tuned defaults.

    Exposes the otherwise-hardcoded scoring dials so a user can tune filtering
    and ranking from their YAML. Any bad value falls back to the default for
    that field. Returns: min_relevance, title_match, abstract_match,
    category_match, weights (normalized, ordinary-paper blend), and
    recency_thresholds (None → use the built-in buckets).
    """
    sc = config.get('scoring') or {}
    if not isinstance(sc, dict):
        sc = {}

    def _num(key, default):
        try:
            return float(sc[key])
        except (KeyError, TypeError, ValueError):
            return default

    # Ranking weights: merge any provided keys onto WEIGHTS_NORMAL, then
    # normalize to sum 1.0 so a partial or off-scale override keeps the 0–10
    # ceiling intact.
    weights = dict(WEIGHTS_NORMAL)
    user_w = sc.get('weights')
    if isinstance(user_w, dict):
        for k in weights:
            try:
                weights[k] = float(user_w[k])
            except (KeyError, TypeError, ValueError):
                pass
    total = sum(weights.values())
    weights = {k: v / total for k, v in weights.items()} if total > 0 else dict(WEIGHTS_NORMAL)

    # Recency buckets: a list of [days, score] pairs, sorted ascending by days.
    thresholds = None
    raw_th = sc.get('recency_thresholds')
    if isinstance(raw_th, list):
        parsed = []
        for item in raw_th:
            try:
                days, score = item
                parsed.append((int(days), float(score)))
            except (TypeError, ValueError):
                continue
        if parsed:
            thresholds = sorted(parsed, key=lambda x: x[0])

    return {
        'min_relevance': _num('min_relevance', MIN_KEYWORD_ONLY_RELEVANCE),
        'min_keyword_matches': max(1, int(_num('min_keyword_matches', 1))),
        'title_match': _num('title_match', RELEVANCE_TITLE_KEYWORD_BOOST),
        'abstract_match': _num('abstract_match', RELEVANCE_SUMMARY_KEYWORD_BOOST),
        'category_match': _num('category_match', RELEVANCE_CATEGORY_MATCH_BOOST),
        'weights': weights,
        'recency_thresholds': thresholds,
    }


def filter_and_score_papers(
    papers: List[Dict],
    config: Dict,
    target_date: Optional[datetime] = None,
    is_hot_paper_batch: bool = False
) -> List[Dict]:
    """
    Filter and score a list of papers.

    Args:
        papers:             List of paper dicts.
        config:             Research configuration.
        target_date:        Reference date for recency scoring.
        is_hot_paper_batch: True when scoring the high-impact (Semantic Scholar) batch.

    Returns:
        Filtered and scored paper list, sorted by recommendation score (descending).
    """
    domains = config.get('research_domains', {})
    excluded_keywords = config.get('excluded_keywords', [])
    sc = _resolve_scoring(config)  # effective scoring dials (config-overridable)

    scored_papers = []

    for paper in papers:
        # Normalize published_date: bio sources store it as "YYYY-MM-DD" string;
        # convert to datetime so all downstream scoring functions work uniformly
        if 'published_date' in paper and isinstance(paper['published_date'], str):
            try:
                paper['published_date'] = datetime.strptime(paper['published_date'], '%Y-%m-%d')
            except (ValueError, TypeError):
                paper['published_date'] = None

        # Compute relevance
        relevance, matched_domain, matched_keywords = calculate_relevance_score(
            paper, domains, excluded_keywords,
            title_boost=sc['title_match'],
            summary_boost=sc['abstract_match'],
            category_boost=sc['category_match'],
        )

        # Scoring is source-neutral: a paper is ranked by how well it matches
        # the user's keywords, not by which site it came from. (No per-source
        # boosts — the source a paper arrives from must not tilt its rank.)

        # Skip papers with zero relevance
        if relevance == 0:
            continue

        # Require that keyword-only relevance (excluding broad arXiv category matches)
        # is sufficient — prevents papers that matched only a category + 1 incidental
        # abstract word from being included.
        n_cat_matches = sum(1 for kw in matched_keywords if ARXIV_CATEGORY_PATTERN.match(kw))
        keyword_only_score = relevance - n_cat_matches * sc['category_match']
        if keyword_only_score < sc['min_relevance']:
            continue

        # Topical-signal gate (QC precision fix): one bare ambiguous word
        # ("alignment", "simulation", "localization") false-matches across fields.
        # Require EITHER >=min_keyword_matches distinct keyword hits, OR >=1 compound
        # keyword (a space/hyphen/&// term like "transposable element", "single-cell",
        # "ATAC-seq"), which is specific enough to stand alone.
        kw_matches = [kw for kw in matched_keywords if not ARXIV_CATEGORY_PATTERN.match(kw)]
        has_compound = any(any(c in kw for c in (' ', '-', '&', '/')) for kw in kw_matches)
        if len(kw_matches) < sc['min_keyword_matches'] and not has_compound:
            continue

        # Compute recency
        if 'published_date' in paper:
            recency = calculate_recency_score(paper.get('published_date'), target_date,
                                              thresholds=sc['recency_thresholds'])
        else:
            # For Semantic Scholar papers, fall back to the publicationDate field
            pub_date_str = paper.get('publicationDate')
            if pub_date_str:
                pub_date = None
                for fmt in ('%Y-%m-%d', '%Y-%m', '%Y'):
                    try:
                        pub_date = datetime.strptime(pub_date_str, fmt)
                        break
                    except (ValueError, TypeError):
                        continue
                recency = calculate_recency_score(
                    pub_date, target_date, thresholds=sc['recency_thresholds']) if pub_date else 0
            else:
                recency = 0

        # Compute popularity
        if is_hot_paper_batch:
            # High-impact batch: derive popularity from influentialCitationCount
            inf_cit = paper.get('influentialCitationCount', 0)
            popularity = min(
                inf_cit / (POPULARITY_INFLUENTIAL_CITATION_FULL_SCORE / SCORE_MAX),
                SCORE_MAX,
            )
        else:
            # Ordinary papers (no citation data): estimate popularity from recency.
            # Very fresh papers may have high potential impact even before citations accrue.
            if 'published_date' in paper and paper['published_date']:
                pub = paper['published_date']
                now = _resolve_reference_now(pub, target_date)
                days_old = (now - pub).days
                if days_old <= 7:
                    popularity = 2.0  # Very new — high potential
                elif days_old <= 14:
                    popularity = 1.5
                elif days_old <= 30:
                    popularity = 1.0
                else:
                    popularity = 0.5
            else:
                popularity = 0.5  # Conservative default when date is unknown

        # Compute quality
        summary = paper.get('summary', '') if 'summary' in paper else paper.get('abstract', '')
        quality = calculate_quality_score(summary)

        # Compute combined recommendation score. Per-domain `priority` weights
        # how a paper RANKS (not whether it passed the gate above): a higher-
        # priority topic's matches get a higher recommendation score.
        weighted_relevance = relevance * _priority_weight(domains, matched_domain)
        recommendation_score = calculate_recommendation_score(
            weighted_relevance, recency, popularity, quality, is_hot_paper_batch,
            # User weights tune the ordinary-paper blend; the high-citation
            # ("hot") batch keeps its popularity-weighted blend.
            weights=None if is_hot_paper_batch else sc['weights'],
        )

        # Attach scoring info to the paper dict
        paper['scores'] = {
            'relevance': round(relevance, 2),
            'recency': round(recency, 2),
            'popularity': round(popularity, 2),
            'quality': round(quality, 2),
            'recommendation': recommendation_score
        }
        paper['matched_domain'] = matched_domain
        paper['matched_keywords'] = matched_keywords
        paper['is_hot_paper'] = is_hot_paper_batch

        scored_papers.append(paper)

    # Sort by recommendation score (descending)
    scored_papers.sort(key=lambda x: x['scores']['recommendation'], reverse=True)

    return scored_papers


def main():
    """Entry point."""
