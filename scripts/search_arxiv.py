def _normalize_authors(authors) -> list:
    """Flatten a mixed-type author list into a plain list[str].

    arXiv returns `["Name1", "Name2"]` (strings); Semantic Scholar
    returns `[{"name": "Name1", "affiliations": [...]}, ...]` (dicts).
    Downstream consumers (the weekly note formatter, save_to_zotero,
    `jq` queries against arxiv_filtered.json) all expect strings.

    Normalising at the source — here in search_arxiv — means
    `arxiv_filtered.json` always has `authors: ["...", ...]`, which is
    `jq`-friendly and removes the band-aid case-split that used to live
    in `save_to_zotero._paper_to_zotero_item`.

    Empty / None inputs return []. Affiliation info is dropped during
    flattening — it lives separately under `paper["affiliations"]`.
    """
    out = []
    for a in authors or []:
        if isinstance(a, dict):
            name = (a.get("name") or "").strip()
        elif isinstance(a, str):
            name = a.strip()
        else:
            name = str(a).strip()
        if name:
            out.append(name)
    return out

