def calculate_date_windows(target_date: Optional[datetime] = None, days: int = 7) -> Tuple[datetime, datetime, datetime, datetime]:
    """
    Compute two date windows: a recent window (default 7 days) and the past year
    excluding that recent window.

    Args:
        target_date: Reference date; defaults to now if None.

    Returns:
        (window_30d_start, window_30d_end, window_1y_start, window_1y_end)
        - window_30d_start: Start of the recent window.
        - window_30d_end:   End of the recent window (= target_date).
        - window_1y_start:  Start of the past-year window.
        - window_1y_end:    End of the past-year window (= day before recent window).
    """
    if target_date is None:
        target_date = datetime.now()
    
    # Recent window: [target_date - days, target_date]
    window_30d_start = target_date - timedelta(days=days)
    window_30d_end = target_date

    # Past-year window (excluding the recent window)
    window_1y_start = target_date - timedelta(days=365)
    window_1y_end = target_date - timedelta(days=days + 1)
    
    return window_30d_start, window_30d_end, window_1y_start, window_1y_end


def search_arxiv_by_date_range(
    categories: List[str],
    start_date: datetime,
    end_date: datetime,
    max_results: int = 200,
    max_retries: int = 3
) -> List[Dict]:
    """
    Search arXiv for papers submitted within a given date range.

    Args:
        categories: List of arXiv category codes.
        start_date: Start of the date range.
        end_date:   End of the date range.
        max_results: Maximum number of results to fetch.
        max_retries: Maximum number of retry attempts.

    Returns:
        List of paper dicts.
    """
    # Build category query
    category_query = "+OR+".join([f"cat:{cat}" for cat in categories])

    # Build date-range query (arXiv uses YYYYMMDD format)
    date_query = f"submittedDate:[{start_date.strftime('%Y%m%d')}0000+TO+{end_date.strftime('%Y%m%d')}2359]"

    # Combine into a single query string
    full_query = f"({category_query})+AND+{date_query}"

    # Build the request URL
    url = (
        f"https://export.arxiv.org/api/query?"
        f"search_query={full_query}&"
        f"max_results={max_results}&"
        f"sortBy=submittedDate&"
        f"sortOrder=descending"
    )
    
    logger.info("[arXiv] Searching papers from %s to %s", start_date.date(), end_date.date())
    logger.debug("[arXiv] URL: %s...", url[:120])
    
    for attempt in range(max_retries):
        try:
            with urllib.request.urlopen(url, timeout=60) as response:
                xml_content = response.read().decode('utf-8')
                papers = parse_arxiv_xml(xml_content)
                logger.info("[arXiv] Found %d papers", len(papers))
                return papers
        except Exception as e:
            logger.warning("[arXiv] Error (attempt %d/%d): %s", attempt + 1, max_retries, e)
            if attempt < max_retries - 1:
                wait_time = (2 ** attempt) * 2
                logger.info("[arXiv] Retrying in %d seconds...", wait_time)
                time.sleep(wait_time)
            else:
                logger.error("[arXiv] Failed after %d attempts", max_retries)
                return []
    
    return []


def search_semantic_scholar_hot_papers(
    query: str,
    start_date: datetime,
    end_date: datetime,
    top_k: int = 20,
    max_retries: int = 3
) -> List[Dict]:
    """
    Search Semantic Scholar for high-impact papers within a given date range.

    Args:
        query:       Search query string.
        start_date:  Start of the date range.
        end_date:    End of the date range.
        top_k:       Return the top-K most influential papers.
        max_retries: Maximum number of retry attempts.

    Returns:
        List of paper dicts sorted by influential citation count (descending).
    """
    # Build date range string (Semantic Scholar uses YYYY-MM-DD:YYYY-MM-DD)
    date_range = f"{start_date.strftime('%Y-%m-%d')}:{end_date.strftime('%Y-%m-%d')}"

    # Build request parameters
    params = {
        "query": query,
        "publicationDateOrYear": date_range,
        "limit": 100,  # Fetch up to 100 most-relevant papers for local re-ranking
        "fields": SEMANTIC_SCHOLAR_FIELDS
    }
    
    headers = {
        "User-Agent": "StartMyWeek-PaperFetcher/1.0"
    }
    if S2_API_KEY:
        headers["x-api-key"] = S2_API_KEY
    
    logger.info("[S2] Searching hot papers from %s to %s", start_date.date(), end_date.date())
    logger.info("[S2] Query: '%s'", query)
    
    for attempt in range(max_retries):
        try:
            if HAS_REQUESTS:
                response = requests.get(
                    SEMANTIC_SCHOLAR_API_URL,
                    params=params,
                    headers=headers,
                    timeout=15
                )
                response.raise_for_status()
                data = response.json()
            else:
                # Fall back to urllib
                query_string = urllib.parse.urlencode(params)
                url = f"{SEMANTIC_SCHOLAR_API_URL}?{query_string}"
                req = urllib.request.Request(url, headers=headers)
                with urllib.request.urlopen(req, timeout=15) as response:
                    data = json.loads(response.read().decode('utf-8'))
            
            papers = data.get("data", [])
            if not papers:
                logger.info("[S2] No papers found")
                return []
            
            # Local secondary filtering and re-ranking
            valid_papers = []
            for p in papers:
                # Skip entries without a title or abstract
                if not p.get("title") or not p.get("abstract"):
                    continue

                # Guard against None values from the API
                inf_cit = p.get("influentialCitationCount") or 0
                cit = p.get("citationCount") or 0
                
                p["influentialCitationCount"] = inf_cit
                p["citationCount"] = cit
                
                # Tag source
                p["source"] = "semantic_scholar"
                p["hot_score"] = inf_cit  # Use influential citation count as the heat score

                # Populate stable `id` from externalIds so --skip-paper-ids works
                ext = p.get("externalIds") or {}
                if ext.get("PubMed"):
                    p["id"] = f"PMID:{ext['PubMed']}"
                elif ext.get("DOI"):
                    p["id"] = f"DOI:{ext['DOI']}"
                elif ext.get("CorpusId"):
                    p["id"] = f"S2:{ext['CorpusId']}"

                # Capture venue / journal name (for display + journal filter)
                venue = p.get("venue") or ""
                if not venue:
                    pv = p.get("publicationVenue") or {}
                    if isinstance(pv, dict):
                        venue = pv.get("name", "") or ""
                p["journal"] = venue

                # Extract affiliation information
                if p.get('authors') and not p.get('affiliations'):
                    affiliations = []
                    for a in p['authors']:
                        if not isinstance(a, dict):
                            continue
                        for affil in (a.get('affiliations') or []):
                            name = affil.get('name', '') if isinstance(affil, dict) else str(affil)
                            if name and name not in affiliations:
                                affiliations.append(name)
                    p['affiliations'] = affiliations

                # Normalise authors to list[str] BEFORE writing to the
                # output dict. Affiliations (extracted above) live
                # separately under p["affiliations"].
                p['authors'] = _normalize_authors(p.get('authors'))

                valid_papers.append(p)
            
            # Sort by influential citation count (descending)
            sorted_papers = sorted(
                valid_papers,
                key=lambda x: x["influentialCitationCount"],
                reverse=True
            )
            
            logger.info("[S2] Found %d valid papers, returning top %d", len(sorted_papers), top_k)
            return sorted_papers[:top_k]
            
        except Exception as e:
            error_msg = str(e)
            logger.warning("[S2] Error (attempt %d/%d): %s", attempt + 1, max_retries, e)
            
            # Check for a 429 Too Many Requests error
            is_rate_limit = False
            if HAS_REQUESTS and hasattr(e, 'response') and e.response is not None:
                is_rate_limit = e.response.status_code == 429
            else:
                is_rate_limit = "429" in error_msg or "Too Many Requests" in error_msg
            
            if attempt < max_retries - 1:
                # Use a longer back-off for 429 rate-limit errors
                if is_rate_limit:
                    wait_time = S2_RATE_LIMIT_WAIT
                    logger.warning("[S2] Rate limit hit. Waiting %d seconds...", wait_time)
                else:
                    wait_time = (2 ** attempt) * 2
                    logger.info("[S2] Retrying in %d seconds...", wait_time)
                time.sleep(wait_time)
            else:
                logger.error("[S2] Failed after %d attempts", max_retries)
                return []
    
    return []


def search_hot_papers_from_categories(
    categories: List[str],
    start_date: datetime,
    end_date: datetime,
    top_k_per_category: int = 5,
    config: Optional[Dict] = None
) -> List[Dict]:
    """
    Search for high-impact papers across multiple arXiv categories.

    Args:
        categories:         List of arXiv category codes.
        start_date:         Start of the date range.
        end_date:           End of the date range.
        top_k_per_category: Number of papers to return per category.
        config:             Research config used to extract user-defined keywords.

    Returns:
        Combined list of high-impact papers.
    """
    all_hot_papers = []
    seen_arxiv_ids = set()

    # Extract user-defined search keywords from the config (more precise than category terms)
    user_queries = []
    if config:
        domains = config.get('research_domains', {})
        for domain_name, domain_config in domains.items():
            keywords = domain_config.get('keywords', [])
            # Combine the first 3 keywords of each domain into a single query
            if keywords:
                query = ' '.join(keywords[:3])
                user_queries.append(query)

    # Fall back to category keywords if no user keywords are available
    if not user_queries:
        user_queries = [ARXIV_CATEGORY_KEYWORDS.get(cat, cat) for cat in categories]

    # Deduplicate queries
    seen_queries = set()
    unique_queries = []
    for q in user_queries:
        q_lower = q.lower()
        if q_lower not in seen_queries:
            seen_queries.add(q_lower)
            unique_queries.append(q)

    for query in unique_queries:
        
        papers = search_semantic_scholar_hot_papers(
            query=query,
            start_date=start_date,
            end_date=end_date,
            top_k=top_k_per_category
        )
        
        # Deduplicate by arXiv ID
        for p in papers:
            # Safely extract the ArXiv ID from the externalIds dict
            arxiv_id = p.get("externalIds", {}).get("ArXiv") if p.get("externalIds") else None

            # Write a unified arxiv_id field for the global dedup in Step 3
            p["arxiv_id"] = arxiv_id

            if arxiv_id and arxiv_id not in seen_arxiv_ids:
                seen_arxiv_ids.add(arxiv_id)
                all_hot_papers.append(p)
            elif not arxiv_id:
                # Keep papers without an arXiv ID (they may come from other sources)
                all_hot_papers.append(p)
        
        time.sleep(S2_CATEGORY_REQUEST_INTERVAL)
    
    # Final sort by influential citation count
    all_hot_papers.sort(key=lambda x: x.get("influentialCitationCount", 0), reverse=True)
    
    return all_hot_papers


def parse_arxiv_xml(xml_content: str) -> List[Dict]:
    """
    Parse an arXiv Atom XML response.

    Args:
        xml_content: Raw XML string returned by the arXiv API.

    Returns:
        List of paper dicts, each containing id, title, authors, abstract, etc.
    """
    papers = []
    
    try:
        root = ET.fromstring(xml_content)
        
        # Iterate over all <entry> elements
        for entry in root.findall('atom:entry', ARXIV_NS):
            paper = {}

            # Extract paper ID
            id_elem = entry.find('atom:id', ARXIV_NS)
            if id_elem is not None:
                paper['id'] = id_elem.text
                # Extract the bare arXiv ID from the URL
                match = re.search(r'arXiv:(\d+\.\d+)', paper['id'])
                if match:
                    paper['arxiv_id'] = match.group(1)
                else:
                    match = re.search(r'/(\d+\.\d+)$', paper['id'])
                    if match:
                        paper['arxiv_id'] = match.group(1)
            
            # Extract title
            title_elem = entry.find('atom:title', ARXIV_NS)
            if title_elem is not None:
                paper['title'] = title_elem.text.strip()

            # Extract abstract
            summary_elem = entry.find('atom:summary', ARXIV_NS)
            if summary_elem is not None:
                paper['summary'] = summary_elem.text.strip()

            # Extract authors (and optional affiliation)
            authors = []
            affiliations = []
            for author in entry.findall('atom:author', ARXIV_NS):
                name_elem = author.find('atom:name', ARXIV_NS)
                if name_elem is not None:
                    authors.append(name_elem.text)
                affil_elem = author.find('arxiv:affiliation', ARXIV_NS)
                if affil_elem is not None and affil_elem.text:
                    affil = affil_elem.text.strip()
                    if affil and affil not in affiliations:
                        affiliations.append(affil)
            paper['authors'] = authors
            paper['affiliations'] = affiliations  # may be an empty list

            # Extract publication date
            published_elem = entry.find('atom:published', ARXIV_NS)
            if published_elem is not None:
                paper['published'] = published_elem.text
                # Parse into a datetime object
                try:
                    paper['published_date'] = datetime.fromisoformat(
                        paper['published'].replace('Z', '+00:00')
                    )
                except (ValueError, TypeError):
                    paper['published_date'] = None
            
            # Extract last-updated date
            updated_elem = entry.find('atom:updated', ARXIV_NS)
            if updated_elem is not None:
                paper['updated'] = updated_elem.text

            # Extract categories
            categories = []
            for category in entry.findall('atom:category', ARXIV_NS):
                term = category.get('term')
                if term:
                    categories.append(term)
            paper['categories'] = categories
            
            # Extract PDF link
            for link in entry.findall('atom:link', ARXIV_NS):
                if link.get('title') == 'pdf':
                    paper['pdf_url'] = link.get('href')
                    break

            # Extract abstract page URL
            if 'id' in paper:
                paper['url'] = paper['id']

            # Tag source
            paper['source'] = 'arxiv'
            
            papers.append(paper)
            
    except ET.ParseError as e:
        logger.error("Error parsing XML: %s", e)
        raise
    
    return papers


